async function seed() {
  console.log("🌱 [SEED] Starting Venue Seeding...");
  for (const v of venues) {
    try {
      const record = await prisma.venueProfile.upsert({
        where: { name: v.name },
        update: { ...v, lastCrawledAt: new Date() },
        create: { ...v, lastCrawledAt: new Date() }
      });
      console.log(`✅ [SEED] Synced Venue: ${record.name}`);
    } catch (e) {
      console.error(`❌ [SEED] Failed for ${v.name}:`, e);
    }
  }
  console.log("🌱 [SEED] Venue Seeding Complete.");
}

seed()
  .catch(err => {
    console.error("Critical Seed Error:", err);
    process.exit(1);
  })
  .finally(() => {
    prisma.$disconnect();
  });
